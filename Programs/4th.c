/* 
1. wap a c program to print an integer entered by the user.
2. wap to add 2 integers 
3. wap to multiply 2 floating point numbers
4. wap to find a ascii value of a character
5. wap a c program to compute quotient and reminder.
*/
#include<stdio.h>
int main()
{
    int a = 10, b = 100;
    float c = 10.5, d = 100.5;

    printf("++a = %d \n", ++a);
    printf("--b = %d \n", --b);
    printf("++c = %f \n", ++c);
    printf("--d = %f \n", --d);

    return 0;
}